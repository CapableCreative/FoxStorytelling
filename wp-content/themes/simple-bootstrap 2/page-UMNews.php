		<h1>Hundreds dead, missing in Sierra Leone mudslides, flooding</h1>
		<p>By Phileas Jusu | Aug. 15, 2017 | FREETOWN, Sierra Leone (UMNS)</p>
	</div>
</div>
<div class="body-container">
<img src="http://foxstorytelling.com/wp-content/uploads/2018/06/Screen-Shot-2018-06-15-at-12.24.20-PM.png">
<br>
<span class="illuminated">United Methodists — including entire families — are among the hundreds killed after torrential rains caused deadly mudslides and flooding near Freetown.</span>
<p>
Capt. John Jimmy and his wife and four children, members of Price Memorial United Methodist Church, were buried under rubble in a mudslide, a relative told United Methodist News Service. Jimmy, a military officer, was the son of the late Rev. Edward Jimmy, a United Methodist worker and clergyman.
</p>
<p>
Sahr Kelly, a member of Charles Davies Memorial United Methodist Church, lost his son and daughter to the flooding on Monday, according to the Rev. Sahr E. K. Fallah, pastor-in-charge. He said the homes of two members had been flooded.
</p>
<p>
The BBC reported that <a href="http://www.bbc.com/news/world-africa-40944540">nearly 400 have been killed</a> and at least 600 are still missing. Those numbers are expected to rise as recovery efforts continue. While much of Freetown was flooded, most of the deaths were in the Kaningo and Regent communities.
</p>
<p>
United Methodist Bishop John K. Yambasu mourned the loss of life “in this needless and preventable disaster.” Yambasu, president of the Sierra Leone Council of Churches, issued a statement on behalf of the council, the largest Christian organization in the country. The bishop and church leaders in other denominations visited the disaster area at Sugar Loaf Mountain on Aug. 16.  
</p>
